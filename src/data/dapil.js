export const DAPIL_BY_PROVINCE = DAPIL_LIST.reduce((acc, d) => {
  if (!acc[d.province]) acc[d.province] = []
  acc[d.province].push(d)
  return acc
}, {})

// ─── Province summary ─────────────────────────────────────────────────────────
export const PROVINCE_SUMMARY = Object.entries(DAPIL_BY_PROVINCE).map(([province_id, dapils]) => {
  const totalSeats = dapils.reduce((s, d) => s + d.seats, 0)
  const totalVoters = dapils.reduce((s, d) => s + d.total_voters, 0)
  const avgTurnout = dapils.reduce((s, d) => s + d.turnout_pct, 0) / dapils.length

  // Aggregate party seats
  const partySeatMap = {}
  dapils.forEach(d => {
    d.parties.forEach(p => {
      partySeatMap[p.id] = (partySeatMap[p.id] || 0) + p.seats
    })
  })
  const sortedParties = Object.entries(partySeatMap)
    .sort((a, b) => b[1] - a[1])
    .map(([id, seats]) => ({ id, seats }))

  const dominantPartyId = sortedParties[0]?.id || null

  return {
    province_id,
    name: PROVINCE_META[province_id]?.name || province_id,
    region: PROVINCE_META[province_id]?.region || 'Lainnya',
    dapil_count: dapils.length,
    total_seats: totalSeats,
    total_voters: totalVoters,
    avg_turnout: Math.round(avgTurnout * 10) / 10,
    dominant_party_id: dominantPartyId,
    party_seats: partySeatMap,
    sorted_parties: sortedParties,
  }
}).sort((a, b) => b.total_seats - a.total_seats)

