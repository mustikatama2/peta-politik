// Scoring weights (must sum to 1.0)
export const SCORECARD_WEIGHTS = {
  ipm: 0.25,
  corruption_risk: 0.20,
  pad_realisasi: 0.15,
  infrastructure: 0.15,
  transparency: 0.15,
  innovation: 0.10,
}

export function calculateScores(provinces) {
  return provinces
    .map(p => {
      const total = Object.entries(SCORECARD_WEIGHTS).reduce(
        (sum, [key, weight]) => sum + (p.scores[key] ?? 50) * weight,
        0,
      )
      return { ...p, total_score: Math.round(total) }
    })
    .sort((a, b) => b.total_score - a.total_score)
    .map((p, i) => ({ ...p, rank: i + 1 }))
}

